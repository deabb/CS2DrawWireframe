        public void DrawWireframe(string[] vertices, List<(int, int)> edges, string offset = "0 0 0")
        {
            foreach (var edge in edges)
            {
                Vector startVertex = ParseVector(vertices[edge.Item1]);
                Vector endVertex = ParseVector(vertices[edge.Item2]);
                DrawLine(startVertex, endVertex, ParseVector(offset));
            }
        }

        public void DrawLine(Vector startPos, Vector endPos, Vector? offset = null)
        {
            // If offset is provided, apply it to the start and end positions
            if (offset != null)
            {
                startPos.X += offset.X;
                startPos.Y += offset.Y;
                startPos.Z += offset.Z;

                endPos.X += offset.X;
                endPos.Y += offset.Y;
                endPos.Z += offset.Z;
            }

            CBeam beam = Utilities.CreateEntityByName<CBeam>("beam")!;
            if (beam == null)
            {
                PrintDebug($"Failed to create beam...");
                return;
            }

            beam.Render = Color.FromName("green");
            beam.Width = 0.5f;

            beam.Teleport(startPos, new QAngle(0, 0, 0), new Vector(0, 0, 0));

            beam.EndPos.X = endPos.X;
            beam.EndPos.Y = endPos.Y;
            beam.EndPos.Z = endPos.Z;

            beam.DispatchSpawn();

            PrintDebug($"Beam Spawned at S:{startPos} E:{beam.EndPos}");
        }

